package CodeingTe;
import java.util.Scanner;

public class FourArithmeticOperations {

	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
		boolean Cheack = false;
		if( !Cheack) {
			int a = in.nextInt();
			int b = in.nextInt();
			System.out.println(a+b);
			System.out.println(a-b);
			System.out.println(a*b);
			System.out.println(a/b);
			System.out.println(a%b);
		}
			
	
	}

}
